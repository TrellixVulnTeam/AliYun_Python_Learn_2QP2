# 元组 tuple
# 元组是一个不可变序列
# 它的操作方式基本上和列表一致
# 所以你在操作元组时，就把元组当成是一个不可变的列表就OK了
# 一般当我们希望数据不改变时，就使用元组，其余情况都使用列表

# 可变对象
#   每个对象都保存了三个数据
#   id（标识）
#   type（类型）
#   value（值）
#   -列表就是一个可变对象
#       a = [1, 2, 3, 4, 5]
#   -a[0] = 10
#       -这个操作是在通过变量去修改对象的值
#       -这种操作不会改变变量所指向的对象
#       -当我们去修改变量时，如果有其他变量椰汁想了该对象，这修改也会在其他变量中体现
#   -a = [4,5,6]
#       -这个操作是再给变量重新赋值
#       -这种操作会改变变量所指向的对象
#       -为一个变量重新赋值时，不会影响其他的变量
#   -一般只有在为变量赋值时才是修改变量，其余的都是修改对象
