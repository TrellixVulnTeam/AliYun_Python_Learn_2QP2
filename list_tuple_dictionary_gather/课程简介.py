# 序列
# 列表（list）
#   -列表是Python中的一个对象
#   -对象（object）就是内存中专门用来存储数据的的一块区域
#   -数值，她只能保存一个单一的数据
#   -列表中可以保存多个有序的数据
#   -列表是用来存储对象的对象
#   -列表的使用：
#       1.列表的创建
#       2.操作列表中的数据
#   - 练习：
#       -创建一个列表，在列表中保存你最好的5个朋友的名字，然后分别通过索引来获取每一个朋友的名字
# 序列（sequence）
#   -序列是Python中最基本的一种数据结构
#   -数据结构是指计算机中数据存储的方式
#   -序列用于保存一组有序的数据，所有的数据在序列当中都有一个唯一的位置（索引），并且序列中的数据会按照添加的顺序来分配索引
#   -序列的分类（序列中的元素能改变）：
#       可变序列：
#           列表（list）
#       不可变序列（序列中的元素不能改变）：
#           字符串（str）
#           元组（tuple）
#       -刚刚我们所讲的所有操作都是序列的通用操作(切片、列表、通用操作)
